/**
 * Казахско-русский словарь для коррекции транскриптов Whisper.
 *
 * Whisper не знает казахский алфавит (ә, ғ, қ, ң, ө, ұ, ү, і, һ)
 * и пишет казахские слова русскими буквами-приближениями.
 *
 * Топ паттерны замен:
 *   қ → к, х     |  ғ → г       |  ң → н, нг
 *   ө → о        |  ұ → у       |  ү → у, ю
 *   ә → а, е     |  і → и       |  һ → х, h
 */

export const ALPHABET_RULES = `
КАЗАХСКИЙ АЛФАВИТ — буквы которых нет в русском:
Ә ә — звук между А и Э. Whisper пишет: а, е, э
Ғ ғ — мягкое Г (фрикативное). Whisper пишет: г
Қ қ — глубокое К (увулярное). Whisper пишет: к, х
Ң ң — носовое Н (как ng). Whisper пишет: н, нг
Ө ө — звук между О и Ё. Whisper пишет: о, е
Ұ ұ — глубокое У. Whisper пишет: у
Ү ү — мягкое У (как немецкое ü). Whisper пишет: у, ю
І і — мягкое И. Whisper пишет: и
Һ һ — придыхательное Х. Whisper пишет: х, h

Главные паттерны (по частоте ошибок):
1. қ→к — самая частая: қоңырау, қанша, қала, қалай, қызмет, қажет, қайда
2. ғ→г — бағасы, тоғыз, ылғал
3. ң→н — қоңырау, таңертең, теңге, сіңлі
4. ү→у — бүгін, үй, күн, түн, жүз, үлкен
5. ұ→у — тұру, ұл, құрғақ, ұзындығы, жұмыс
6. ө→о — көру, өте, төлеу, бөлме, көше, сөйлеу
7. ә→а — сәлем, әке, әже, әдемі
8. і→и — кілем, сіз, біз, тіпті, білу, істеу
`

