/**
 * Применяет программные замены к тексту Whisper.
 * Возвращает { corrected, changes } для логирования.
 */
export function applyDictionaryFixes(text: string): { corrected: string; changes: string[] } {
  const changes: string[] = []
  let result = text

  for (const [pattern, replacement] of REPLACEMENTS) {
    const before = result
    result = result.replace(pattern, replacement)
    if (result !== before) {
      // Extract what changed
      const match = before.match(pattern)
      if (match) changes.push(`"${match[0]}" → "${replacement}"`)
    }
  }

  return { corrected: result, changes }
}

